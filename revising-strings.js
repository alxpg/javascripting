let pizza ='pizza is alright';
pizza = pizza.replace('alright', 'wonderful');
console.log(pizza);

// imprime: pizza is wonderful porque la variable pizza es un string que se reemplaza con el método replace. El método replace toma dos argumentos, el primer argumento es la cadena que se va a reemplazar y el segundo argumento es la cadena que se va a agregar en su lugar. En este caso, 'alright' se reemplaza con 'wonderful'.//